<h1>General Rules</h1>
<ul>
  <li>Competitors may dance at adjacent levels in different styles but may not span more than 2 levels. For example - registering for Silver Smooth and Bronze Latin is fine but registering for Gold Smooth and Bronze Latin is not. If a dance is not offered at the level you are competing, you may dance up for that dance (e.g. a Newcomer dancer can dance Silver Viennese Waltz since it it not offered at a lower level).</li>
  <p></p>

  <li>Double registration (i.e. registering for subsequent levels in the same style) is allowed.</li>
  <li>Couples must dance at the level of the higher competitor (i.e. if the Follower is Bronze, and the Leader is Silver, the couple must dance at the Silver level and have the option of double registering to also compete at the Gold level, if they so desire). </li>
  <li>There are <strong>no gender restrictions</strong> on couples.</li>
</ul>

  <h4>Level Eligability</h4>
    <ul>
      <li>Newcomer: Have not danced prior to the Spring 2018 semester.</li>
      <li>YCN points should be used to determine level eligibility for Bronze, Silver, Gold, and Open/Pre-Champ. Please use our <a href="calculator">YCN point calculator</a> to determine level eligibility.
      </li>
      <li>
        <ul>
        <li>To be eligible to compete you must have less than 7 points in the dance at that level. </li>
        <li>If you have pointed out of a dance that is linked to another, you cannot dance in either of them (e.g. if a rhythm leader has 6 points in chacha, rumba, and mambo, but has 7 points in swing, he cannot complete in Silver Swing/Mamboâ€”because they are linked events. He may still compete in Silver Chacha/Rumba, and is welcome to double register for the gold rhythm events.)</li>
        </ul>
      </li>
      </li>
      <li>Syllabus events are open to all competitors.</li>
    </ul>
  </li>
  <h4>Syllabi</h4>
  <ul>
    <li>Competitors should follow the ISTD syllabus for International styles and the NDCA syllabus for American style dances for Newcomer, Bronze, Silver, and Gold levels.</li>
    <li>Open/ Pre-Champ competitors are not restricted to a syllabus but lifts will not be permitted.</li>
  </ul>
  <h4>Costumes</h4>
  <ul>
    <li>Costumes are permitted at the Bronze level and above.</li>
    <li>Heel protectors (plastic or suede) are required for all high-heeled shoes.</li>
  </ul>
