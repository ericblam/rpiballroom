<p>In addition to the <i>standard</i> set of events we have (see what we did there?), we hold numerous other events for your fun and enjoyment. We have <b>Open Bronze</b> events (more below), <b>fun dances</b> throughout the day (including a traditional West Coast Swing at the end of the day), and (new this year) <b>Peabody!</b><p>

<h3>What is Open Bronze?</h3>
<p>You'll see them listed below as "Open Smooth" and "Open Rhythm" but they are still under the Bronze level!  What does this mean?  We are giving Bronze dancers the opportunity to get a little creative with their choreography!  Dancers should stay within Bronze level dancing (i.e. no continuity, etc.) but they are welcome to introduce open figures and are not limited to strict rules of lead-follow!  Make sense?  If you have any questions, feel free to ask!</p>
<p>All Newcomer and Bronze dancers are invited to participate in these events in addition to their traditional dances.  Note:  In the registration system, they will be designated as "Open Bronze" instead of Smooth or Rhythm to distinguish them from the category events.</p>

<h3>Peabody?</h3>
<p>Many years ago, Peabody was competed in the Smooth category. It was similar in style to the Foxtrot, though the Peabody was danced to much faster music. It would be one of the elements in creating the Quickstep. A little bit of history can be found <a href="http://www.streetswing.com/histmain/z3pbody.htm">here</a>.</p>
<p>For a few examples of Peabody, there is everything from <a href="https://www.youtube.com/watch?v=BaUWqU0AQGQ">this Peabody by Mark and Brenda Judkins</a> to <a href="https://www.youtube.com/watch?v=_VOFkpsxuv0">Sandra Cameron and Paul Grecki dancing the Peabody at the Dance of the Year celebration in 1991</a> to <a href="https://www.youtube.com/watch?v=naOfHtYJX0w">Chad Fasca and Midori Asakura at the 2004 American Lindy Hop Championships</a>.</p>
<p>There is not currently much available in terms of a syllabus, but it is possible to find the <a href="/smooth_bronze.pdf">bronze</a> and <a href="/smooth_silver.pdf">silver</a> figure names, to give you an idea of the dance.</p>
<p>Though Peabody is listed under syllabus, because of the lack of syllabus available, it will be danced somewhat more as a fun dance. Make sure to keep it in the style and spirit of the dance!</p>

