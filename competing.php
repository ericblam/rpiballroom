<p>You can find information on variations between the International and American Styles, as well as the official syllabi, in USA Dance's <a href="http://usadance.org/dancesport/forms-and-resources/rules-policies-and-bylaws/">DanceSport Rulebook.</a></p>
<h3>The Dance Floor</h3>
<p>Couples compete against others of similar skill level in the following levels:</p>
<ul>
  <li>Newcomer/Pre-Bronze</li>
  <li>Bronze</li>
  <li>Silver</li>
  <li>Gold</li>
  <li>Pre-Champion</li>
  <li>Champion</li>
</ul>
<p>The Newcomer through Gold levels are syllabus levels where figures are restricted to those officially designated. Higher levels (considered "Open") do not have syllabus restrictions.</p>
<p>For additional information on competition, the Columbia University Ballroom Dance Team provides a great resource: <a href="http://www.columbiaballroom.org/files/Competition%20Guide.pdf"> Ballroom Dance Competition Guide</a>.</p>
