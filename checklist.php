<h1 class="title">Competition Checklist</h1>					
<h3>General:</h3>
<ul>
  <li>Change of clothes</li>
  <li>Comfortable (flat) shoes</li>
  <li>Change of socks</li>
  <li>RPI Ballroom jacket and/or other swag (Represent!)</li>
  <li>Snacks and drinks</li>
  <li>Mints or gum</li>
  <li>Water bottle</li>
  <li>Money</li>
  <li>Pen/pencil</li>
  <li>Phone (and charger if needed)</li>
  <li>Glasses case</li>
  <li>Your A-Game</li>
</ul>
<h3>Lady Costume:</h3>
<ul>
  <li>Smooth/Standard dress</li>
  <li>Rhythm/Latin dress</li>
  <li>Dance shorts</li>
  <li>Nylon or tights (check for snags/tears)</li>
  <li>Proper undergarments</li>
</ul>
<h3>Gentleman Costume:</h3>
<ul>
  <li>Smooth/Standard shirt (preferably white)</li>
  <li>Latin/Rhythm shirt (black)</li>
  <li>Tie (preferably matching partner's smooth dress, black otherwise)</li>
  <li>Vest (if needed)</li>
  <li>Undershirt</li>
  <li>Pants (black)</li>
  <li>Belt</li>
  <li>Socks (black)</li>
</ul>
<h3>Shoes:</h3>
<ul>
  <li>Competition shoes (clean and brushed)</li>
  <li>Shoe brush</li>
  <li>Vaseline (to prevent patent shoes from sticking)</li>
  <li>Heel protectors or suede</li>
</ul>
<h3>Appearance:</h3>
<ul>
  <li>Hair products</li>
  <li>Makeup</li>
  <li>Jewelry</li>
</ul>
<h3>Maintenance:</h3>
<ul>
  <li>Deodorant (You will sweat. A lot)</li>
  <li>Safety pins</li>
  <li>Double-sided tape</li>
  <li>Bobby pins</li>
  <li>Hairspray</li>
  <li>Band-aids</li>
  <li>Moleskin</li>
  <li>Contact solution</li>
</ul>
<h3>Overnight Items:</h3>
<ul>
  <li>Toiletries</li>
  <li>Towel</li>
  <li>Pillow</li>
  <li>Sleeping mat/bag (if needed)</li>
  <li>Blanket</li>
</ul>
