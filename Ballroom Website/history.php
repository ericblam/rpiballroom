<h1 class="title">History</h1>

<p>The RPI Ballroom Club was started in April 1989.</p>

<p>Dr Robert Athanasiou, Dr. of Wellness at RPI thought that the students were not getting enough exercise and asked Leslie Laguardia, owner of Arthur Murray Dance Studio, if she would start the program. He eventually was able to get the students gym and class credit for taking the program.</p>

<p>The first year there were only around 30 kids. In 1995 when the GAP commercial came out with Lindy hoppers in khakis, the classgrew to 300 kids, 4 classes a night.</p>

<p>Around 1994 the team started and has been holding competitions annually for over 15 years.</p>
