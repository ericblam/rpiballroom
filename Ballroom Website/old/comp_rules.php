    <ul>
<li> &bull; Competitors may dance at adjacent levels in different styles but may not span more than 2 levels. For example - registering for Silver Smooth and Bronze Latin is fine but registering for Gold Smooth and Bronze Latin is not. </li>
<p></p>
<li> &bull; Double registration (i.e. registering for subsequent levels in the same style) is allowed.
<ul>
<li> - Couples must dance at the level of the higher competitor (i.e. if the Follower is Bronze, and the Leader is Silver, the couple must dance at the Silver level and have the option of double registering to also compete at the Gold level, if they so desire).
</li>
</ul>
</li><li> &bull; Level Eligability:
<ul>
<li> - Newcomer: Have not danced prior to the Spring 2015 semester. </li>
<li> - YCN points should be used to determine level eligibility for Bronze, Silver, Gold, and Open/Pre-Champ </li>
<li> - Syllabus events are open to all competitors. </li>
</ul>
</li><li> &bull; Competitors should follow the ISTD syllabus for International styles and the NDCA syllabus for American style dances for Newcomer, Bronze, Silver, and Gold levels. Open/ Pre-Champ competitors are not restricted to a syllabus but lifts will not be permitted.
</li>
<p></p>
<li> &bull; All is fair in love and <del>war</del> Fun Dances. </li>
<p></p>
<li> &bull; Costumes are permitted at the Bronze level and above. </li>
<p></p>
<li> &bull; Heel protectors (plastic or suede) are required for all high-heeled shoes. </li>
</ul>  </div>

