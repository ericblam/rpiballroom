    <p>Throughout the year, RPI Ballroom hosts and participates in signature events. Check the <a href="template.php?page=news">news page</a> or the <a href="template.php?page=calendar">calendar</a> for event detail announcements, including dates and locations.</p>


<h3>Dancing with the RPI Stars</h3>
<p>Watch out for this grand annual spectacle! Witness RPI stars-professors and faculty-dance with the student ballroom stars in a show that's guaranteed to impress. See star-pro couples compete on stage and face each other in a light-hearted competition for the adoration of the student body.</p>


<h3>NRB Ballroom Blitz</h3>
<p> A "Navigating Rensselaer and Beyond" day event, Ballroom Blitz offers incoming freshmen an opportunity to experience a sampling of Argentine Tango, and Lindy Hop Swing, and Ballroom/Latin dancing, with instructors and upper-class students. This event is hosted by the RPI Ballroom club including participation from all four sections. Throughout the day, students will rotate through multiple dance styles. This is a great opportunity for new students to learn some fun dance moves, and get acquainted with our clubs!</p>


<h3>The Introduction Dance</h3>
<p>At the beginning of each semester, RPI Ballroom throws this social dance for prospective dancers. One of our instructors holds a free lesson for attendees, which is followed by a social dance. The Introduction Dance aims to provide a general idea of what the Club is like, as well as a chance to meet new people.</p>


<h3>Jump, Jive, and Jazz!</h3>
<p>Swing dance late into the night to the live music of RPI's Jazz Ensembles and The Campus Serenaders! This semesterly event draws crowds that fills the room with the energy that music and dancing are all about. Don't miss the opportunity to practice your swing moves learned thus far.</p>


<h3>La Milonga: The Tango Social</h3>
<p>Spend a day learning the Argentine Tango, the dance of passion. Join us for workshops introducing the dance, its style, and figures, all leading to the night's social Milonga.</p>


<h3>RPI Dancesport Competition</h3>
<p>Held in the Spring, RPI and the Ballroom Team hosts this regional DanceSport competition. Attended by dozens of schools and institutions, this event not only gives a chance for Team members to compete. It is open to spectators and lasts the entire day.</p>


<h3>Social Dance Outings</h3>
<p>RPI Ballroom regularly organizes excursions to local Capital District dance events and venues like the monthly <a href=http://capitalusadance.com/">Capital USA Dance </a>, the <a href=http://www.danceflurry.org/swing">First Friday Capital Swing Dance</a>, and the <a href=http://www.saratogasavoy.com/files/diamonddance.html">Saratoga Savoy Diamond Dance</a>.</p>  </div>

