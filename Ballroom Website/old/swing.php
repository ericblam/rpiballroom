    <h3>What is the Lindy Hop track?</h3>
<p>Lessons in Lindy Hop began as the Swing Club under RPI Ballroom, founded to support a growing interest in swing dancing on the RPI campus back in the Fall of 2006. The group's first President, Diana Shiroma, along with the first Vice President, Brooks Prumo, wanted to spread their love of the dance and provide a place where others could learn the lost art. Through the past couple of years, RPI Ballroom has begun holding regular Lindy Hop workshops and dances for the campus and community. The track offers lessons in Lindy Hop and other various dances from the 1920s to the 1940s, including the Charleston. Lessons and a practice session are held once a week for members to expand their knowledge of the dance.</p>
<h3>What Will I Learn?</h3>
<p>In the beginner Lindy Hop I class, you will learn the basics of Lindy. This lesson series will teach Charleston basic, basic in open &amp; sendout to open, underarm turn &amp; tuck turn, swingout, lindy circle.</p>
<p>After you have the basics down, join us for the intermediate Lindy Hop II class! We will build on what was taught in the beginner class. We highly encourage you to take this class more than once as our instructor teaches different material each time.</p>
<h3>Do I Need a Partner?</h3>
<p>You don't need to bring a partner for any of these classes. We rotate partners so the men dance with all the ladies and the ladies dance with all the men. If there is an unbalanced gender situation, then whichever group has more simply takes turns during the rotation. People are also welcome to learn the opposite part (for example, ladies can learn to lead or men can learn to follow).</p>
<h3>What Should I Wear?</h3>
<ul>
<li>(1) Wear clothes that are comfortable and are not restrictive to your movement (for example, long tight skirts are not recommended).</li>
<li>(2) Wear shoes with flat soles (high heels are not recommended) that grip your ankle, so they don't fly off when you kick.</li>
<li>(3) You also want light shoes without a lot of traction (hiking boots are bad), so you can spin with less strain on your knees.</li>
<li>(4) For beginners, comfortable dress shoes (like loafters or wing tips) or light gym shoes are fine to start.</li>
<li>(5) It's a class, not a social event, so you aren't obligated to dress up. Wear something that won't make you feel self-conscious. We also recommend that you be aware of hygiene (deodorant and toothpaste are your friends). If you tend to sweat a lot, consider bringing an extra shirt or towel and a bottle of water.</li>
</ul>
