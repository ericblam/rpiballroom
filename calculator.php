<h3><strong> Understanding YCN Points </strong></h3>
<p>The YCN point system determines eligibility for the Bronze, Silver, Gold, and Pre-Champ levels.</p>
<ul>
  <li>To be eligible to compete you must have less than 7 points in the dance at that level.</li>
  <li>If you have pointed out of a dance that is linked to another, you cannot dance in either of them (e.g. if a rhythm leader has 6 points in chacha, rumba, and mambo, but has 7 points in swing, he cannot complete in Silver Swing/Mamboâ€”because they are linked events. He may still compete in Silver Chacha/Rumba, and can double register in Gold rhythm if the competition allows double registration. If double registration is prohibited, he can only dance Gold rhythm.)</li>
 </ul>
