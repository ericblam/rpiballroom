<h1 class="title">Social Dancing</h1>

<h3>What are the Social Dances?</h3>
<img class="img-responsive" style="float:right;padding:5px" width="400px" src="site_images/socialdance.jpg" />

<p>The social dances are on-campus events hosted by the RPI Ballroom Club. There’s a short dance lesson, followed by music to dance to throughout the night. Each social is based on a theme and dance, be it Country Two-Step, 50’s Swing, or the annual Halloween Masquerave!</p>
<p>The socials are a great way to forget about all of school’s stresses and meet some new people! The atmosphere is fun and lively, and you might even discover a new passion!</p>
<p>Neither experience nor a partner is required or expected. We’ll teach everyone how to dance and make sure there’s always club members to help.</p>


<h3>OK I want to come. Now what?</h3>
<p>The Ballroom Club hosts social dances roughly once a month during the fall and spring semesters. Posters will be put up around campus the week before each dance, so wach for those. The best way to keep up with new dances is to <a href= "https://www.facebook.com/RPIBallroom/">follow the club on Facebook </a>, as all events are posted there. You will even find a way to purchase discounted tickets! </p>
<p>The dances are usually set on Fridays at 7 PM, in the Academy Hall Auditorium on the 3rd floor. Admission is $5 at the door, $2 if signed up early, and FREE for all members of the ballroom club. However, up to date pricing, location, and time will be on all posters and on Facebook.</p>

<p> The <strong> social dances this semester will be on: </strong></p>
<ul><strong>
  <li> September 22nd - Disco Party with Hustle Lesson</li>
  <li> November 3rd - Annual Masquerave with Tango Lesson </li>
  <li> December 4th - Holiday Party with Rumba Lesson </li>
</strong></ul>

<p>None of these events are club specific so please invite your friends! </p>

<h3>Do I Need a Partner? What Should I Wear?</h3>
<p>You don't need a partner because we continuously rotate throughout the class, making it possible for everyone to dance with and meet new people. You should wear clothes in which you are comfortable moving around and shoes that do not stick to the floor. Many Team members wear ballroom shoes with suede on the bottom, though socks or sneakers work just as well (and are much less expensive)!</p>
