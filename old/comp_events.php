    <p><b><i>BACK THIS YEAR!  We've added some additional Bronze events!</i></b><br />
      You'll see them listed below as "Open Smooth" and "Open Rhythm" but they are still under the Bronze level!  What does this mean?  We are giving Bronze dancers the opportunity to get a little creative with their choreography!  Dancers should stay within Bronze level dancing (i.e. no continuity, etc.) but they are welcome to introduce open figures and are not limited to strict rules of lead-follow!  Make sense?  If you have any questions -feel free to ask!</p>
    <p>All Newcomer and Bronze dancers are invited to participate in these events in addition to their traditional dances.  Note:  In the registration system, they will be designated as "Open Bronze" instead of Smooth or Rhythm to distinguish them from the category events.</p>
