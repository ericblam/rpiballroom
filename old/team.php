    <h3>What is the Ballroom Team?</h3>
<p>The Ballroom Team is composed of dedicated dancers of all skill levels. Most members compete in regional collegiate DanceSport competitions and strive to improve continuously. As a result, Team members commit more time to progress their dancing abilities.</p>
<p>Two of the five weekly sessions are split between lessons and open practice. On these days, newer and veteran members receive concurrent lessons suited for their level of experience.</p>
<p>Anyone can join the Team. New members do not need previous experience-only an interest in ballroom dance.</p>
<h3>When Do We Practice?</h3>
<p>See the class schedule!</p>
<p>Of course, members do not have to attend every practice or stay for entire durations. Most do not due to personal time constraints; however, as with any skill, advancement only happens with time and dedication.</p>
<h3>Do I Need a Partner?</h3>
<p>Nope!  We do not expect new members, regardless of skill level, to have a partner.  It is common for new members to dance with multiple people before settling on a partner.  While we offer guidance for new members, especially for the first few competitions, many partnerships naturally occur after some time.</p>
