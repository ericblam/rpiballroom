<h1 class="title">Syllabus Steps</h1>			
<p>It is the hope that this list will be useful and will provide a consistent platform for the development of new dancers. This list is by no means complete, but it contains to the best of our knowledge steps that are valid under various American Syllabi.</p>
<p>For help with the syllabus, you can contact one of our mentors. <a href="mentors">Check out when they are available!</a></p>
<p>For an older version of our syllabus, please go <a href="syllabus_steps_old1">here</a>.</p>
