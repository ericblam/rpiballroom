<h1 class="title">Ballroom &amp; Latin</h1>

<h3>What is the Ballroom &amp; Latin track?</h3>
<img class="img-responsive" style="float:right;padding:5px" width="400px" src="site_images/latin.jpg" />
<p>The Ballroom &amp; Latin track is the largest and most general of the four tracks. Geared towards preparing students for social dancing, the track offers three classes per week -- Beginner, Intermediate, and Advanced -- every semester.</p>

<p>Ballroom &amp; Latin covers a great breadth of dances in a relaxed, collaborative environment. Beginner lessons are great for new dancers who want a general introduction to the different ballroom dances. Those who have completed at least two semesters of beginner lessons are suggested to move up to intermediate, though students are always welcome to try out any level. The intermediate classes builds on the basics of several dances, focusing on technique and building more complicated patterns. Many Team members attend these lessons for supplemental knowledge and review.</p>

<h3>What Will I Learn?</h3>
<p>Ballroom &amp; Latin covers a wide range of dances throughout the year, with the second semester continuing the work of the first. You will learn:</p>
<ul>
  <li>Cha Cha</li>
  <li>East Coast Swing</li>
  <li>Foxtrot</li>
  <li>Hustle</li>
  <li>Merengue</li>
  <li>Salsa</li>
  <li>Rumba</li>
  <li>Tango</li>
  <li>Waltz</li>
  <li>West Coast Swing</li>
</ul>

<p>At the beginning of each class, we review material taught previously in the semester, easing the loss of missing a class or two.</p>

<h3>Do I Need a Partner? What Should I Wear?</h3>
<p>You don't need a partner because we continuously rotate throughout the class, making it possible for everyone to dance with and meet new people. You should wear clothes in which you are comfortable moving around and shoes that do not stick to the floor. Many Team members wear ballroom shoes with suede on the bottom, though socks work just as well (and are much less expensive)!</p>
