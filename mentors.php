<h1 class="title">Team Mentors</h1>
<p>Our team mentors are here to make sure you are familiar with the <a href="syllabus_steps">syllabus steps</a>, technique, and maybe even help you out with learning a new step.</p>
<p>Make sure to visit our mentor during their office hours! Mentors are higher-level dancers that are available for specific dances or styles and are willing to help you as much as they can. If you can only make lesson times or a practice time with no scheduled mentors, check if they can meet with you then. Email them or the <a href="mailto:ballroom-captains@union.rpi.edu?Subject=Mentor%20Times">Team Captains</a></p>

